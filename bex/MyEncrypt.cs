        public static String encrypt(String msg, String userName, String pwd)
        {
            String str = "";
            msg = exc(msg);
            userName = exc(userName);
            pwd = exc(pwd);
            byte[] bys1 = Encoding.Default.GetBytes(msg);
            byte[] bys2 = Encoding.Default.GetBytes(userName);
            byte[] bys3 = Encoding.Default.GetBytes(pwd);
            long j = 0;
            for (int i = 0; i < bys1.Length; i++)
            {
                j += 2 << bys1[i];
            }
            for (int i = 0; i < bys2.Length; i++)
            {
                j += 3 << bys2[i];
            }
            for (int i = 0; i < bys3.Length; i++)
            {
                j += 4 << bys3[i];
            }
            String s = "" + j;
            String[] js = split(s);
            int c = 0;
            for (int i = 0; i < js.Length; i++)
            {
                c = int.Parse(js[i]);
                c = c << c;
                str += code[c];
            }
            return str;
        }

        public static String exc(String s)
        {
            String s1 = DateTime.Now.ToString("yyyyMMddHH");
            s = GetMD5(s);
            s1 = GetMD5(s1);
            return s.Length > s1.Length ? s + s1 : s1 + s;
        }

        public static string GetMD5(string str)
        {
            MD5 md5 = new MD5CryptoServiceProvider();
            byte[] fromData = System.Text.Encoding.UTF8.GetBytes(str);
            byte[] targetData = md5.ComputeHash(fromData);
            string byte2String = "";
            for (int i = 0; i < targetData.Length; i++)
            {
                byte2String += targetData[i].ToString("x2");
            }
            return byte2String;
        }

        public static string GetMd5_16(string ConvertString, bool isTower)
        {
            MD5CryptoServiceProvider md5 = new MD5CryptoServiceProvider();
            string t2 = BitConverter.ToString(md5.ComputeHash(UTF8Encoding.Default.GetBytes(ConvertString)), 4, 8);
            t2 = t2.Replace("-", "");
            if (isTower)
            {
                t2 = t2.ToLower();
            }
            return t2;
        }

